arr = [3, 5, 2, 8, 1]

def max_num(arr):
    max_value = arr[0]     # assume first element is max
    
    for num in arr:        # iterate through each value
        if num > max_value:
            max_value = num   # update max
    
    print(max_value)

max_num(arr)


